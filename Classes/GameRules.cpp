#include "GameRules.h"

const std::string& GameRules::summary() {
    static const std::string rules = R"(游戏规则（精简版）
1. 起始配置
   - 玩家生命 20，护甲 0，能量 3，饥饿 6，经验 0。
   - 起手抽 5 张卡，回合开始额外抽 1 张。

2. 回合流程
   - 结算天气与生物群系效果。
   - 抽牌，能量回复至基础值（默认 3，可被建筑影响）。
   - 行动阶段：打出卡牌、合成、战斗、建造。
   - 回合结束：若饥饿为 0，受到 2 点真实伤害。

3. 资源与合成
   - 资源牌提供材料点，累积到配方要求即可合成。
   - 常见配方：木板 + 木棒 = 木剑；铁锭 + 木棒 = 铁剑；
     钻石 + 木棒 = 钻石剑；铁锭 + 皮革 = 铁甲。

4. 战斗
   - 武器与怪物卡具有攻击力。
   - 怪物攻击优先度：僵尸 > 骷髅 > 苦力怕 > 蜘蛛。
   - 护甲抵消伤害，耐久归零时损坏。

5. 生物群系
   - 森林：木材类卡牌费用 -1。
   - 沙漠：怪物卡攻击 +1，食物卡效果 -1。
   - 雪地：行动阶段第一张工具卡免费。
   - 下界：火焰与烈焰棒相关卡牌效果 +1。
   - 末地：抽牌阶段额外抽 1 张，但怪物攻击 +1。

6. 天气
   - 晴天：无效果。
   - 雨天：水相关卡牌效果 +1。
   - 雷暴：回合结束有 50% 触发“闪电”事件。

7. 胜负条件
   - 胜利：击败末影龙或建造并激活末地传送门。
   - 失败：生命值归零或连续三回合饥饿为 0。
)";
    return rules;
}
